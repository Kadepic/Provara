def contexte_epingle() -> ssl.SSLContext:
    """Contexte TLS dont les ancres = les racines EMBARQUÉES ci-dessus (et rien d'autre : le magasin système,
    potentiellement pollué, n'est pas consulté). Mêmes réglages durcis que ssl.create_default_context."""
    return ssl.create_default_context(cadata=_ANCRES_PEM)


def _echec_de_certificat(e: Exception) -> bool:
    """L'échec vient-il de la VALIDATION du certificat (et non du réseau/DNS/HTTP) ? Seul ce cas justifie le repli."""
    if isinstance(e, ssl.SSLCertVerificationError):
        return True
    raison = getattr(e, "reason", None)          # urllib enveloppe : URLError(reason=SSLCertVerificationError)
    return isinstance(raison, ssl.SSLCertVerificationError)


def urlopen(url_ou_req, timeout: float = 20):
    """urllib.request.urlopen + repli épinglé sur échec de certificat. Toute AUTRE erreur remonte inchangée
    (un 404/timeout/DNS n'est pas un problème de confiance). L'appelant garde sa gestion d'erreur habituelle."""
    try:
        return urllib.request.urlopen(url_ou_req, timeout=timeout)
    except (urllib.error.URLError, ssl.SSLError) as e:
        if not _echec_de_certificat(e):
            raise
    return urllib.request.urlopen(url_ou_req, timeout=timeout, context=contexte_epingle())
